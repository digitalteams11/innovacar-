-- Fixes POST /api/maintenance and PATCH /api/maintenance/{id}/status 500s that
-- happen after the core write succeeds: the maintenance work order/vehicle
-- status changes commit fine, but the follow-up notification insert is
-- rejected by notifications_type_check because it predates the MAINTENANCE_*
-- notification types, which marks the transaction rollback-only and surfaces
-- as UnexpectedRollbackException.
--
-- This migration is intentionally non-destructive: no rows are deleted, and
-- the existing constraint (whatever its current name/definition) is dropped
-- and rebuilt with every value the Notification.NotificationType enum
-- supports today, so it can't drift out of sync with the entity again.

-- Any row already violating the new list is normalized to INFORMATION instead
-- of being deleted, so historical notifications are preserved.
UPDATE notifications
SET type = 'INFORMATION'
WHERE type IS NULL OR type NOT IN (
